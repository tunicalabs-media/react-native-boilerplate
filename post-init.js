const fs = require('fs');
const path = require('path');

function renameIfNeeded(from, to) {
  try {
    if (fs.existsSync(from) && !fs.existsSync(to)) {
      fs.renameSync(from, to);
    }
  } catch (error) {
    console.error(`Failed to rename ${path.basename(from)}:`, error);
  }
}

